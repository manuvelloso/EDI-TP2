#include <stdint.h>
#include <stdio.h>

int main()
{
	int16_t num1 = 0x00FF; //0000 0000 1111 1111
	int16_t num2 = 0x0012; //0000 0000 0001 0010

	int16_t num3 = 0x005F; //0000 0000 0101 1111
	int16_t num4 = 0x00FA; //0000 0000 1111 1010

	int16_t num5 = 0xAA55; //1010 1010 0101 0101
	int16_t num6 = 0x55AA; //0101 0101 1010 1010

	int16_t num7 = 0xFFFF; //1111 1111 1111 1111
	int16_t num8 = 0x1212; //0001 0010 0001 0010

	//AND
	//0000 0000 1111 1111 & 0000 0000 0001 0010 = 0000 0000 0001 0010 = 0x0012
	//0000 0000 0101 1111 & 0000 0000 1111 1010 = 0000 0000 0101 1010 = 0x005A
	//1010 1010 0101 0101 & 0101 0101 1010 1010 = 0000 0000 0000 0000 = 0x0000
	//1111 1111 1111 1111 & 0001 0010 0001 0010 = 0001 0010 0001 0010 = 0x1212
	printf("num1 & num2: 0x%x\n", num1 & num2);
	printf("num3 & num4: 0x%x\n", num3 & num4);
	printf("num5 & num6: 0x%x\n", num5 & num6);
	printf("num7 & num8: 0x%x\n", num7 & num8);

	//OR
	//0000 0000 1111 1111 | 0000 0000 0001 0010 = 0000 0000 1111 1111 = 0x00FF
	//0000 0000 0101 1111 | 0000 0000 1111 1010 = 0000 0000 1111 1111 = 0x00FF
	//1010 1010 0101 0101 | 0101 0101 1010 1010 = 1111 1111 1111 1111 = 0xFFFF
	//1111 1111 1111 1111 | 0001 0010 0001 0010 = 1111 1111 1111 1111 = 0xFFFF
	printf("num1 | num2: 0x%x\n", num1 | num2);
	printf("num3 | num4: 0x%x\n", num3 | num4);
	printf("num5 | num6: 0x%x\n", num5 | num6);
	printf("num7 | num8: 0x%x\n", num7 | num8);

	//XOR
	//0000 0000 1111 1111 | 0000 0000 0001 0010 = 0000 0000 1110 1101 = 0x00ED
	//0000 0000 0101 1111 | 0000 0000 1111 1010 = 0000 0000 1010 0101 = 0x00A5
	//1010 1010 0101 0101 | 0101 0101 1010 1010 = 1111 1111 1111 1111 = 0xFFFF
	//1111 1111 1111 1111 | 0001 0010 0001 0010 = 1110 1101 1110 1101 = 0xEDED
	printf("num1 ^ num2: 0x%x\n", num1 ^ num2);
	printf("num3 ^ num4: 0x%x\n", num3 ^ num4);
	printf("num5 ^ num6: 0x%x\n", num5 ^ num6);
	printf("num7 ^ num8: 0x%x\n", num7 ^ num8);

	return 0;
}